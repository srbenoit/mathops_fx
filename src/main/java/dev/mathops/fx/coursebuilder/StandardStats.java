package dev.mathops.fx.coursebuilder;

/**
 * A container for statistics for single standard.
 */
class StandardStats {

    /** The number of intro lessons. */
    int numIntroLessons = 0;

    /** The number of summary lessons. */
    int numSummaryLessons = 0;

    /** The number of objectives. */
    int numObjectives = 0;

    /** The number of lessons within objectives. */
    int numObjectiveLessons = 0;

    /** The number of examples within objectives. */
    int numObjectiveExamples = 0;

    /** The number of examples. */
    int numExamples = 0;

    /** The number of explorations. */
    int numExplorations = 0;

    /** The number of lessons within explorations. */
    int numExplorationLessons = 0;

    /** The number of examples within explorations. */
    int numExplorationExamples = 0;

    /** The number of applications. */
    int numApplications = 0;

    /** The number of lessons within applications. */
    int numApplicationLessons = 0;

    /** The number of examples within applications. */
    int numApplicationExamples = 0;

    /** The number of handouts. */
    int numHandouts = 0;

    /** The number of items (including items in objectives, explorations, and applications). */
    int numItems = 0;

    /** The number of assessments. */
    int numAssessments = 0;

    /**
     * Constructs a new {@code StandardStats}.
     */
    StandardStats() {

        // No action
    }
}
